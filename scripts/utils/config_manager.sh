#!/bin/bash
#
# Configuration Manager - Unified Configuration System
#
# This script manages the unified configuration system that eliminates
# redundant configuration parameters across multiple files.
#
# Usage:
#   ./config_manager.sh generate    # Generate all config files from master
#   ./config_manager.sh validate    # Validate master configuration
#   ./config_manager.sh sync       # Sync changes to all config files
#   ./config_manager.sh clean      # Remove generated config files
#

set -euo pipefail

# Script directory and paths
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
CONFIG_DIR="$PROJECT_ROOT/config"

# Master configuration file
MASTER_CONFIG="$CONFIG_DIR/deployment.conf"

# Color codes for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Logging functions
log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

log_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

log_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1" >&2
}

# Load master configuration
load_master_config() {
    if [[ ! -f "$MASTER_CONFIG" ]]; then
        log_error "Master configuration file not found: $MASTER_CONFIG"
        exit 1
    fi
    
    log_info "Loading master configuration: $MASTER_CONFIG"
    
    # Source the configuration file safely
    while IFS='=' read -r key value; do
        # Skip comments and empty lines
        [[ $key =~ ^[[:space:]]*# ]] && continue
        [[ -z $key ]] && continue
        
        # Remove quotes, comments, and whitespace
        key=$(echo "$key" | tr -d ' ')
        value=$(echo "$value" | sed 's/[[:space:]]*#.*//' | sed 's/^["'"'"']\|["'"'"']$//g' | xargs)
        
        # Export the variable
        export "$key"="$value"
    done < "$MASTER_CONFIG"
    
    log_success "Master configuration loaded successfully"
}

