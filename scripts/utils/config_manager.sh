# Validate master configuration
validate_config() {
    log_info "Validating master configuration..."
    
    local errors=0
    
    # Check required fields
    local required_fields=(
        "HOSTNAME" "USERNAME" "TIMEZONE" "LOCALE" "KEYMAP" 
        "DEPLOYMENT_PROFILE" "DESKTOP_SESSION" "DISPLAY_MANAGER"
    )
    
    for field in "${required_fields[@]}"; do
        if [[ -z "${!field:-}" ]]; then
            log_error "Required field $field is empty or not set"
            ((errors++))
        fi
    done
    
    # Validate profile
    if [[ ! "${DEPLOYMENT_PROFILE}" =~ ^(work|personal|development)$ ]]; then
        log_error "Invalid DEPLOYMENT_PROFILE: ${DEPLOYMENT_PROFILE}. Must be: work, personal, or development"
        ((errors++))
    fi
    
    # Validate password mode
    if [[ ! "${PASSWORD_MODE}" =~ ^(env|file|generate|interactive)$ ]]; then
        log_error "Invalid PASSWORD_MODE: ${PASSWORD_MODE}. Must be: env, file, generate, or interactive"
        ((errors++))
    fi
    
    # Validate boolean values
    local boolean_fields=(
        "DISK_ENCRYPTION" "ENABLE_FIREWALL" "ENABLE_FAIL2BAN" "ENABLE_AUDIT"
        "ENABLE_SSH" "ENABLE_AUR" "AUTO_LOGIN" "AUTO_REBOOT"
    )
    
    for field in "${boolean_fields[@]}"; do
        if [[ ! "${!field:-}" =~ ^(true|false)$ ]]; then
            log_error "Invalid boolean value for $field: ${!field}. Must be: true or false"
            ((errors++))
        fi
    done
    
    if [[ $errors -eq 0 ]]; then
        log_success "Configuration validation passed"
        return 0
    else
        log_error "Configuration validation failed with $errors errors"
        return 1
    fi
}

# Generate all configuration files
generate_all_configs() {
    log_info "Generating all configuration files from master configuration..."
    
    load_master_config
    
    if ! validate_config; then
        log_error "Configuration validation failed. Aborting generation."
        exit 1
    fi
    
    # Generate all configuration files
    generate_bootstrap_config
    generate_deploy_config
    generate_example_deployment_yaml
    generate_ansible_group_vars
    generate_profile_ansible_vars
    generate_archinstall_config
    
    log_success "All configuration files generated successfully"
}

# Clean generated configuration files
clean_configs() {
    log_info "Cleaning generated configuration files..."
    
    local files_to_clean=(
        "$PROJECT_ROOT/bootstrap.conf"
        "$CONFIG_DIR/deploy.conf"
        "$PROJECT_ROOT/example_deployment_config.yml"
        "$PROJECT_ROOT/configs/ansible/group_vars/all/vars.yml"
        "$PROJECT_ROOT/configs/profiles/*/ansible/vars.yml"
        "$PROJECT_ROOT/configs/profiles/*/archinstall/user_configuration.json"
    )
    
    for file_pattern in "${files_to_clean[@]}"; do
        if [[ "$file_pattern" == *"*"* ]]; then
            # Handle glob patterns
            for file in $file_pattern; do
                if [[ -f "$file" ]]; then
                    rm "$file"
                    log_info "Removed: $file"
                fi
            done
        else
            # Handle exact file paths
            if [[ -f "$file_pattern" ]]; then
                rm "$file_pattern"
                log_info "Removed: $file_pattern"
            fi
        fi
    done
    
    log_success "Configuration cleanup complete"
}

# Show usage information
show_usage() {
    cat << EOF
Configuration Manager - Unified Configuration System

USAGE:
    $(basename "$0") COMMAND

COMMANDS:
    generate    Generate all configuration files from master config
    validate    Validate master configuration file
    sync        Sync changes to all configuration files (same as generate)
    clean       Remove all generated configuration files
    help        Show this help message

DESCRIPTION:
    This script manages a unified configuration system that eliminates
    redundant configuration parameters across multiple files. All
    configuration is centralized in config/deployment.conf.

EXAMPLES:
    $(basename "$0") generate    # Generate all config files
    $(basename "$0") validate    # Check master config validity
    $(basename "$0") clean       # Remove generated files

EOF
}

# Main function
main() {
    case "${1:-}" in
        generate|sync)
            generate_all_configs
            ;;
        validate)
            load_master_config
            validate_config
            ;;
        clean)
            clean_configs
            ;;
        help|--help|-h)
            show_usage
            ;;
        "")
            log_error "No command specified"
            show_usage
            exit 1
            ;;
        *)
            log_error "Unknown command: $1"
            show_usage
            exit 1
            ;;
    esac
}

# Run main function if script is executed directly
